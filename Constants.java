/**
 * Represents all the constant variables.
 */
public class Constants {
    /**
     * Determines board's x size.
     */
    public static int SIZE_X;

    /**
     * Determines board's y size.
     */
    public static int SIZE_Y;

    /**
     * Determines the numbers of mines on the board.
     */
    public static int MINE_AMOUNT;

    /**
     * Determines character for closed cells.
     */
    public static final char CLOSED = '+';

    /**
     * Determines character for mine.
     */
    public static final char MINE = '@';

    /**
     * Determines character for open cells.
     */
    public static final char OPEN = ' ';
}
